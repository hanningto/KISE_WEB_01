// Function with parameters in JavaScript

// Function that takes two parameters: a name (string) and an age (integer)
function introduce(name, age) {
    console.log("My name is " + name + " and I am " + age + " years old.");
}

// Calling the function with different arguments
introduce("Alice", 25); // Output: My name is Alice and I am 25 years old.
introduce("Bob", 30);   // Output: My name is Bob and I am 30 years old.
